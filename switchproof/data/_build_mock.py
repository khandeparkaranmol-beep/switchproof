def main() -> None:
    out = Path(__file__).resolve().parent / "banking77_mock.jsonl"
    rows = []
    for intent, texts in INTENTS.items():
        for t in texts:
            rows.append({"text": t, "label": intent})
    with open(out, "w", encoding="utf-8") as fh:
        for r in rows:
            fh.write(json.dumps(r) + "\n")
    print(f"Wrote {len(rows)} rows across {len(INTENTS)} intents -> {out.name}")


if __name__ == "__main__":
    main()
